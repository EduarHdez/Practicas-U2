    private String Multi(int n,int i){
        
       if(i==0){
            return ("Tabla del "+n+":\n")+Multi(n, (i+1));
        }else{
            if(i==10){
                s+=n;
                return n+" X 10 = "+s;
            }else{
                s+=n;
                return (n+" X "+i+" = "+s+"\n")+Multi(n, (i+1));  
            }
        }
    }

